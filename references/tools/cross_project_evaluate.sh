#!/bin/bash
# eval_pipeline.sh — Automated cross-project validation pipeline
#
# For a NEW project (with its own memory/principles/ dir), automatically:
# 1. Run cross_project_check.sh to ask the 4 diagnostic questions
# 2. Run check_stale.sh + propagate.sh on the new principles
# 3. Compare to existing Layer 3 principles (template given)
# 4. Generate validation_report.md
#
# Usage:
#   bash eval_pipeline.sh <new_project_dir> <existing_principles_file>
#
#   <new_project_dir>: path to new project (e.g., ../store-sales)
#   <existing_principles_file>: path to your canonical principles .md
#                            (e.g., memory/principles/walmart-extraction-2026-06.md)
#
# Output: <new_project_dir>/validation_report.md
#
# The pipeline does NOT modify the existing principles file. It generates
# a new report that you review, then manually update evidence_count if
# you agree the new project provides evidence.

set -e

if [ -z "$1" ] || [ -z "$2" ]; then
  echo "Usage: bash eval_pipeline.sh <new_project_dir> <existing_principles_file>"
  exit 1
fi

NEW_PROJECT_DIR="$1"
EXISTING_PRINCIPLES_FILE="$2"

if [ ! -d "$NEW_PROJECT_DIR" ]; then
  echo "ERROR: $NEW_PROJECT_DIR is not a directory"
  exit 1
fi

if [ ! -f "$EXISTING_PRINCIPLES_FILE" ]; then
  echo "ERROR: $EXISTING_PRINCIPLES_FILE is not a file"
  exit 1
fi

REPORT="$NEW_PROJECT_DIR/validation_report.md"
TODAY=$(date +%Y-%m-%d)

echo "=== Cross-Project Evaluation Pipeline ==="
echo "New project: $NEW_PROJECT_DIR"
echo "Existing principles: $EXISTING_PRINCIPLES_FILE"
echo "Output report: $REPORT"
echo ""

# Step 1: Find new project's principles
echo "[1/4] Discovering principles in $NEW_PROJECT_DIR..."
NEW_PRINCIPLES_DIR="$NEW_PROJECT_DIR/memory/principles"
if [ -d "$NEW_PRINCIPLES_DIR" ]; then
  PRINCIPLES_DIR="$NEW_PRINCIPLES_DIR" bash "$EXISTING_PRINCIPLES_FILE" 2>/dev/null || true
  PRINCIPLES_DIR="$NEW_PRINCIPLES_DIR" bash "$(dirname "$0")/cross_project_check.sh" > /tmp/_eval_diag.txt 2>&1 || true
  n_diag=$(grep -c "Principle " /tmp/_eval_diag.txt || echo 0)
  echo "  Found $n_diag principle(s) in new project"
else
  echo "  No $NEW_PRINCIPLES_DIR — using new_project_dir itself"
  PRINCIPLES_DIR="$NEW_PROJECT_DIR" bash "$(dirname "$0")/cross_project_check.sh" > /tmp/_eval_diag.txt 2>&1 || true
  n_diag=$(grep -c "Principle " /tmp/_eval_diag.txt || echo 0)
  echo "  Found $n_diag principle(s)"
fi

# Step 2: Run lifecycle tools on existing principles to get current state
echo ""
echo "[2/4] Running check_stale.sh on existing principles..."
PRINCIPLES_DIR="$(dirname "$EXISTING_PRINCIPLES_FILE")" bash "$(dirname "$0")/check_stale.sh" > /tmp/_eval_stale.txt 2>&1 || true
echo "  Output saved to /tmp/_eval_stale.txt"

echo ""
echo "[3/4] Running propagate.sh on existing principles..."
PRINCIPLES_DIR="$(dirname "$EXISTING_PRINCIPLES_FILE")" bash "$(dirname "$0")/propagate.sh" > /tmp/_eval_propagate.txt 2>&1 || true
echo "  Output saved to /tmp/_eval_propagate.txt"

# Step 3: Read current evidence count from existing principles
echo ""
echo "[4/4] Reading current state from existing principles..."
current_evidence=$(grep "evidence_count:" "$EXISTING_PRINCIPLES_FILE" | head -1 | awk '{print $2}')
current_status=$(grep "status:" "$EXISTING_PRINCIPLES_FILE" | head -1 | awk '{print $2}')
echo "  Current status: $current_status"
echo "  Current evidence_count: $current_evidence"

# Step 4: Generate report
cat > "$REPORT" <<EOF
# Cross-Project Validation Report

> **Generated by**: eval_pipeline.sh
> **Date**: $TODAY
> **New project**: \`$NEW_PROJECT_DIR\`
> **Existing principles**: \`$EXISTING_PRINCIPLES_FILE\`

## Step 1: Principles in New Project

EOF

cat /tmp/_eval_diag.txt >> "$REPORT"
echo "" >> "$REPORT"

cat >> "$REPORT" <<EOF

## Step 2: Current Lifecycle State (existing principles)

\`\`\`
$(cat /tmp/_eval_stale.txt)
\`\`\`

## Step 3: Bellman Propagation (existing principles)

\`\`\`
$(head -25 /tmp/_eval_propagate.txt)
\`\`\`

## Step 4: Decision

| Question | Recommendation |
|----------|----------------|
| Should evidence_count be incremented? | $([ "$n_diag" -gt 0 ] && echo "YES" || echo "NO (no principles found in new project)") |
| New evidence_count value | $((current_evidence + 1)) |
| New last_validated | $TODAY |

## Action

If you confirm the new project provides evidence:

1. Edit the existing principles file
2. Update each principle's metadata:
   - \`evidence_count: $current_evidence\` → \`$((current_evidence + 1))\`
   - \`last_validated: <old>\` → \`$TODAY\`
3. Run \`bash hybrid_validate.sh --all\` to verify
4. Commit with message: "feat: cross-project validation #<N> (<project name>)"

## Limitations

This pipeline does NOT:
- Auto-modify the existing principles file (intentional, to prevent silent corruption)
- Decide FOR you whether the new project is valid (you decide)
- Generate validation answers (you must read the diagnostic and answer)

It only provides:
- Discovery (what principles exist in the new project)
- Lifecycle state (how the existing principles are doing)
- Bellman propagation (cross-principle utility)
- A template for the decision (increment or not)

EOF

# Cleanup
rm -f /tmp/_eval_diag.txt /tmp/_eval_stale.txt /tmp/_eval_propagate.txt

echo ""
echo "=== Report generated: $REPORT ==="
echo ""
echo "Next step: review $REPORT and manually update the existing"
echo "principles file if you agree the new project provides evidence."
